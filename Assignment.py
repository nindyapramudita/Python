#Rumus

def ditambah(x,y): #Penjumlahan
    return x+y

def dikurang(x,y): #Pengurangan
    return x - y

def dikali(x,y): #Pengalian
    return x * y

def dibagi(x,y): #Pembagian
    return x / y

def dimod(x,y): #modulus
    return x % y

def dicoba(x,y,z): #percobaan dalam bentuk lain
    return x % y + z

def dicoba2(x,y,z,a): #percobaan dalam bentuk lain
    return x % y + z * a





